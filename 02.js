let num1 = 10;
console.log(num1);
console.log(typeof num1);

//실수(float)
let num2 = 3.14;
console.log(num2);
console.log(typeof num2);

//지수형(Exp)
let num3 = 2.5e5; //2.5 x 10^5
console.log(num3);
console.log(typeof num3);

//Nan : Not a Number
let num4 = "Hello" / 2;
console.log(num4);

//Infinity (무한대)
let num5 = 1 / 0;
console.log(num5);
console.log(typeof num5);

//-Infinity (무한대)
let num6 = -1 / 0;
console.log(num6);
console.log(typeof num6);

